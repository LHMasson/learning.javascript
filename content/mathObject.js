// Set Variable
let num1 = 9.32

// Round a Number (Floor / Ceil / Round)
let roundFloor = Math.floor(num1); // 9
console.log(roundFloor);
let roundCeil = Math.ceil(num1); // 10
console.log(roundCeil);
let round = Math.round(num1); // 9
console.log(round);

// Search a number in a list of numbers
console.log(Math.max(1,2,3,4,5,-10,5)); // Max Value - 5
console.log(Math.min(1,2,3,4,5,-10,5)) // Minimun Value - -10

// Create random values
console.log(Math.random()); // 0 - 1 Number

// PI Value
console.log(Math.PI); // 3.141592653589793

// Potentiation
console.log(Math.pow(2, 10)); // 1024
console.log(2**10); // 1024

// Rooting
console.log(9**0.5) // 3
