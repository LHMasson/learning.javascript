// Round Number
let num1 = 1.12332;
console.log(num1.toFixed(2)); // 1.12

// Verify if a variable is a Integer
console.log(Number.isInteger(num1)); // false

// Verify if is Not A Number
console.log(Number.isNaN(num1)); // false

// Parse
console.log(parseFloat(num1)); // 1.12332
console.log(parseInt(num1)); // 1
